#include "function.h"

void	one(int t[][4])
{
	ft_add(t[0], 4, 1, 2);
	ft_add(t[1], 4, 1, 3);
	ft_add(t[2], 4, 2, 1);
	ft_add(t[3], 4, 2, 3);
	ft_add(t[4], 4, 3, 1);
	ft_add(t[5], 4, 3, 2);
	t[6][0] = 0;
}

void	two(int t[][4])
{
	ft_add(t[0], 1, 4, 2);
	ft_add(t[1], 1, 4, 3);
	ft_add(t[2], 2, 1, 4);
	ft_add(t[3], 2, 4, 1);
	ft_add(t[4], 2, 4, 3);
	ft_add(t[5], 3, 1, 2);
	ft_add(t[6], 3, 1, 4);
	ft_add(t[7], 3, 2, 1);
	ft_add(t[8], 3, 2, 4);
	ft_add(t[9], 3, 4, 1);
	ft_add(t[10], 3, 4, 2);
	t[11][0] = 0;
}

void	three(int t[][4])
{
	ft_add(t[0], 1, 2, 4);
	ft_add(t[1], 1, 3, 4);
	ft_add(t[2], 1, 3, 2);
	ft_add(t[3], 2, 1, 3);
	ft_add(t[4], 2, 3, 1);
	ft_add(t[5], 2, 3, 4);
	t[6][0] = 0;
}

void	four(int t[][4])
{
	ft_add(t[0], 1, 2, 3);
	t[1][0] = 0;
}

void	ft_init(int s[4][12][4])
{
	one(s[0]);
	two(s[1]);
	three(s[2]);
	four(s[3]);
}
